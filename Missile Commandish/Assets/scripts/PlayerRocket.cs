   * Start *
  *****************************************************************************/
  protected override void Start()
    {
    base.Start();
    GameManager.updatePlayerRocketCount(-1);
    }

  /*****************************************************************************
   * OnCollisionEnter2D *
  *****************************************************************************/
  protected override void OnTriggerEnter2D(Collider2D collision)
    {
    base.OnTriggerEnter2D(collision);
    }

  /*****************************************************************************
   * Methods 
  *****************************************************************************/

  }
